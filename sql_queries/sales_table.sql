CREATE TABLE sales_data (
    Order_ID INT,
    Customer_Name VARCHAR(100),
    Product VARCHAR(100),
    Category VARCHAR(100),
    Region VARCHAR(50),
    Sales FLOAT,
    Profit FLOAT,
    Quantity INT,
    Order_Date DATE,
    Month VARCHAR(20),
    Year INT,
    Customer_Type VARCHAR(50),
    Payment_Mode VARCHAR(50)
);